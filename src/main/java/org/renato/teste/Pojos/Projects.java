package com.sample;


public class Projects {

  private long project_Id;
  private String name;
  private long companys_Company_Id;


  public long getProject_Id() {
    return project_Id;
  }

  public void setProject_Id(long project_Id) {
    this.project_Id = project_Id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getCompanys_Company_Id() {
    return companys_Company_Id;
  }

  public void setCompanys_Company_Id(long companys_Company_Id) {
    this.companys_Company_Id = companys_Company_Id;
  }

}
